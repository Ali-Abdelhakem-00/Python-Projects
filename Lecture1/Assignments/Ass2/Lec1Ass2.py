Num1 = input("Enter First Number: ")
Num2 = input("Enter Second Number: ")


print("Summation of the two numbers = ", int(Num1) + int(Num2))
print("Subtraction of the two numbers = ", int(Num1) - int(Num2))
print("Multiplication of the two numbers = ", int(Num1) * int(Num2))
print("Division of the two numbers = ", int(Num1) / int(Num2))
print("Floor Division of the two numbers = ", int(Num1) // int(Num2))
print("Exponent(power) of the two numbers = ", int(Num1) ** int(Num2))
